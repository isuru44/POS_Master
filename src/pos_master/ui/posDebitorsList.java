    private void refresh3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refresh3ActionPerformed
        // TODO add your handling code here:
        DBConnection db=new DBConnection();
        db.connectDb();
        try{
            db.st=db.con.createStatement();
            String sql="SELECT * FROM debitors";
            db.rs=db.st.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) tblDebitors.getModel();
            while(db.rs.next()){
                model.addRow(new Object[]{db.rs.getString(1),db.rs.getString(2),db.rs.getString(3)});
            }

        }catch(Exception e){
            System.out.println(e);
        }
        db.closeDb();

    }//GEN-LAST:event_refresh3ActionPerformed

