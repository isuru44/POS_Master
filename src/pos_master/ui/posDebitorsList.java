    private void btnAddDebitorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDebitorActionPerformed
        // TODO add your handling code here:
        if(! txtDebitorName.getText().equals("")&& ! txtDebitorContact.getText().equals("")){

            DBConnection db = new DBConnection();
            db.connectDb();
            try {
                String sql="INSERT INTO debitors (iddebitors,debitorname,debitorcontact) VALUES(?,?,?)";
                db.ps=db.con.prepareStatement(sql);
                db.ps.setString(1,txtDebitorID.getText());
                db.ps.setString(2,txtDebitorName.getText());
                db.ps.setString(3,txtDebitorContact.getText());

                db.ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Debitor added Successfuly","Success",JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }finally{
                db.closeDb();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Fill All Details","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddDebitorActionPerformed

